  class Utility
    def self.robot?(user_agent)
      user_agent =~ /\b(Baidu|Gigabot|Google|libwww-perl|lwp-trivial|msnbot|SiteUptime|Slurp|WordPress|ZIBB|ZyBorg)\b/i
    end
  end

